    private void transferBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferBtnActionPerformed
        List<Map<String,String>> users = User.getUsers();
        DefaultTableModel m = (DefaultTableModel) userTable.getModel();
        userTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        m.setRowCount(0);    
        for(final Map<String,String> item : users) {
            if(!item.get("accountNo").equals(ATMController.getInstance().getModel().getAccountNo())){
                 m.addRow(new String[]{item.get("accountNo"),item.get("fullname")});
            }  
        }
        transferDialog.getContentPane().setBackground(new Color(0,153,255) );
        transferDialog.setSize(300, 500);
        transferDialog.pack();
        transferDialog.setLocation((dim.width - transferDialog.getWidth()) / 2, (dim.height - transferDialog.getHeight()) / 2);
        transferDialog.setResizable(false);
        
        transferDialog.setVisible(true);
       
        
    }//GEN-LAST:event_transferBtnActionPerformed

    private void withdrawalConfirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawalConfirmBtnActionPerformed
       Integer amount = Integer.parseInt(withDrawAmountTF.getText());
       boolean success = ATMController.getInstance().doWithdrawal(amount);
       if(amount==0){
           JOptionPane.showMessageDialog(null, "Silahkan masukkkan jumlah tarik tunai yang valid (min Rp 1)","AMOUNT INVALID",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
       if(success){
           JOptionPane.showMessageDialog(null, "Tarik Tunai Berhasil","STATUS TARIK TUNAI",JOptionPane.INFORMATION_MESSAGE);
       }else{
            JOptionPane.showMessageDialog(null, "Tarik Tunai Gagal\nPastikan saldo anda masih mencukupi","STATUS TARIK TUNAI",JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_withdrawalConfirmBtnActionPerformed

    private void trfConfirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trfConfirmBtnActionPerformed
        Integer amount = Integer.parseInt(trfAmountTF.getText());
        int accountcolumn = 0;
        int row = userTable.getSelectedRow();
        
        if(row==-1){
            JOptionPane.showMessageDialog(null, "Anda belum memilih tujuan transfer","TUJUAN TRANSFER",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(amount==0){
           JOptionPane.showMessageDialog(null, "Silahkan masukkkan jumlah tarik tunai yang valid (min Rp 1)","AMOUNT INVALID",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
        System.out.println(row);
        String to = (String)userTable.getValueAt(row, accountcolumn);
        
        boolean success = ATMController.getInstance().doTransfer(amount, to);
        if(success){
           JOptionPane.showMessageDialog(null, "Tranfer senilai " + amount+ " ke rekening " + to+" berhasil\nSilahkan cek saldo anda","STATUS TRANSFER",JOptionPane.INFORMATION_MESSAGE);
           
       }else{
            JOptionPane.showMessageDialog(null, "Gagal transfer\nPastikan saldo anda mencukupi","STATUS TRANSFER",JOptionPane.ERROR_MESSAGE);
            
        }
        
        
       
    }//GEN-LAST:event_trfConfirmBtnActionPerformed

