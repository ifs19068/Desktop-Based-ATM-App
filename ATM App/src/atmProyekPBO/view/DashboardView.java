    private class LengthLimitKeyAdapter extends KeyAdapter{
        @Override
        public void keyTyped(KeyEvent e) {
            JTextField input  = (JTextField) e.getSource();
            if(input.getText().length()==1 && input.getText().equals("0")){
                input.setText("");
            }
            if(input.getText().length()>9){
                getToolkit().beep();
                e.consume();
            }
            
        }
    }
    
    private class OnlyNumberKeyAdapter extends KeyAdapter{
        @Override
        public void keyTyped(KeyEvent e) {
            
            char c = e.getKeyChar();
            if (!((c >= '0') && (c <= '9') ||
               (c == KeyEvent.VK_BACK_SPACE) ||
               (c == KeyEvent.VK_DELETE))) {
              getToolkit().beep();
              e.consume();
            }
        }
       
    }
    
    
    
    

    

